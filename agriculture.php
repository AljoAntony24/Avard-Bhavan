                <h2>Agriculture Department </h2>
                <p>Agriculture development through a holistic approach is our vision, aiming for a sustainable development in the life of common people depending on agriculture for their livelihood. Our objective is to develop a blend of traditional cultivation norms in assistance with modern technology by which we can produce qualitative and quantitative products that neither hinder the ecological balance nor create any environmental hazards.
                </p>
                <b>Joint Liability Group (JLG)</b>
                <p>AVARD has successfully implemented the Joint Liability Groups, a financial inclusion programme of NABARD in collaboration with Bank of India for supporting the farmers in achieving a sustainable livelihood from agriculture. The JLG programme is structured to promote group farming. JLGs should comprise 4 members each in a group and should be recommended by AVARD. The group farming imbibes trust among them and creates a rapport between their families too. The proficiency of the groups will bud magnificent results in developing creative farming methods. They also make value added products from their farms which in turn becomes an additional income generation. The successful promotion of all these opens another opportunity to gain more profit. The credit linkage facility accelerates their interest in farming and the group farming practices helps them in achieving sustainable income generation from agriculture.
                </p>
                

