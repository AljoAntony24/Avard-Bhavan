                </b>
                <p>Service Providing Centre (SPC) aided by Women and Child Development Department (WCD) started functioning in the year 2008. SPC mainly focuses on cases of DV Act –Protection of Women from Domestic Violence Act enacted in the year 2005. The centre has a Legal Counsellor who provides free legal counseling & legal aid to the clients on Monday, Wednesday and Friday from 2pm to 5pm.
                </p>
