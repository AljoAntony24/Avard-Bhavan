                <h2>Family Development</h2>
                <p>Family is the fundamental unit of human society. The Family development programme aims to help families meet basic needs as well as to promote self reliance and sustainability.</p>
                <b>FAMILY COUNSELING CENTER
                    <br/>
                </b>

                <p>Family Counseling Centre (FCC) aided by Central Social Welfare Board (CSWB) started functioning in the year 1987.  The FCC is well staffed with qualified and experienced Counselors, Legal counselor, Social Workers and other office bearers. FCC functions everyday from 10.00 a.m. to 5.00 pm except on Sundays. FCC provides free individual counseling, family counseling, Student Counselling and Pre-marital Counselling where Cases are referred from WPO (Women Protection Officer),Police stations, representatives of  Block Panchayth and Municipality , Advocates, Social Workers and schools. </p>
                <p>During this year 630 new cases and 1220 follow-up cases were handled at the centre; among them 718 were closed. The centre also conducted 35 awareness classes for students, women, Parents, Teachers, Anganawadi workers and the Differently abled and also conducted rally and Signature campaign on Women’s day.
                </p>
